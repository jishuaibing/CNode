// The default language is English
var lang = "en";
// Check for localStorage support
if ('localStorage' in window) {
    var lang = localStorage.getItem('lang') || navigator.language.slice(0, 2);
    if (!Object.keys(arrLang).includes(lang)) {
        lang = 'en';
    }
}
$(document).ready(function () {
    console.log(localStorage.getItem('lang'));
    if (localStorage.getItem('lang') == 'en' || localStorage.getItem('lang') == null) {
        $('.circleen').addClass('active')
        $('.circlezh').removeClass('active')
        $(".lang").each(function (index, element) {
            var word = arrLang['en'][$(this).attr("key")]
            $(this).text(word);
            // var word1 = arrLang[lang][$(this).attr("placeholder")]
        });
    } else if (localStorage.getItem('lang') == 'zh') {
        $('.circlezh').addClass('active')
        $('.circleen').removeClass('active')
        $(".lang").each(function (index, element) {
            var word = arrLang['zh'][$(this).attr("key")]
            $(this).text(word);
            // var word1 = arrLang[lang][$(this).attr("placeholder")]
        });
    }
});
// get/set the selected language
$(".translate").click(function () {
    var lang = $(this).attr("id");
    if (lang == 'zh') {
        $('.circleen').removeClass('active')
        $('.circlezh').addClass('active')
    } else {
        $('.circlezh').removeClass('active')
        $('.circleen').addClass('active')
    }
    // update localStorage key
    if ('localStorage' in window) {
        localStorage.setItem('lang', lang);
        console.log(localStorage.getItem('lang'));
    }
    $(".lang").each(function (index, element) {
        var word = arrLang[lang][$(this).attr("key")]
        // var word1 = arrLang[lang][$(this).attr("placeholder")]
        $(this).text(word);
    });
});